package fundamentojava;

public class NumeroPar {
    static void main() {
        for(int i = 0;i <= 100;i++){

            if (i%2 == 0){
                System.out.println("NumeroPar" + i );
            }else{
                System.out.println("Numero Inpar"+ i);
            }

        }

    }
}
